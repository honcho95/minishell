#include "ft_printf.h"

void	ft_putstr(const char *s, t_data *data)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		if (write (1, &s[i++], 1) != -1)
			data->nbr_return += 1;
	}
}
